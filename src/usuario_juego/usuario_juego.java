package usuario_juego;

public class usuario_juego {

	
	private int idcli;
	private int idpro;
	
	
	
	
	public usuario_juego(
					
				int idcli, int idpro){  
				
		this.idcli=idcli;
		this.idpro=idpro;
		

		
	}




	public int getIdcli() {
		return idcli;
	}




	public void setIdcli(int idcli) {
		this.idcli = idcli;
	}




	public int getIdpro() {
		return idpro;
	}




	public void setIdpro(int idpro) {
		this.idpro = idpro;
	}






		
		
}

